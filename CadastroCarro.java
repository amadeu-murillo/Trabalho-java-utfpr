    public void consCarro() {
    Carro ca = new Carro();

    if (cxPlaca.getText().isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "Digite a placa para consultar o carro.",
            "Campo Vazio",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se o campo de placa estiver vazio
    }
    
    try {
        ca.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	ca =(Carro) ex.corPlaca(ca);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    ca= GerCarro.geraGerCarro().consCarroPlaca(ca); // Realiza a consulta do carro pela placa
    
    if (ca != null) {
        // Exibir informa칞칫es do carro encontrado nos campos correspondentes
        cxPlaca.setText(ca.getPlaca());
        cxCor.setText(ca.getCor());
        cxQtdePassageiro.setText(String.valueOf(ca.getQtdePassageiro()));
        cxTarifa.setText(String.valueOf(ca.getTarifa()));
        cxTipoCorrida.setText(ca.getCorrida());
        cxDistancia.setText(String.valueOf(ca.getTarifa()));

        // Exibir mensagem indicando que o carro foi encontrado
        JOptionPane.showMessageDialog(
            null,
            "CARRO ENCONTRADO",
            "CONSULTA DE CARRO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        // Limpar campos se o carro n칚o for encontrado e exibir mensagem de erro
        limpar();
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "CARRO N츾O CADASTRADO",
            JOptionPane.ERROR_MESSAGE
        );
    }
}

    public void altCarro() {
    Carro ca = new Carro();
    
    if (camposVazios()) {
        JOptionPane.showMessageDialog(
            null,
            "Preencha todos os campos para alterar os dados do carro.",
            "Campos Vazios",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se houver campos vazios
    }

    try {
        ca.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	ca =(Carro) ex.corPlaca(ca);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    ca = GerCarro.geraGerCarro().atualizaCarro(ca); // Atualiza o carro com base na placa fornecida
    
    if (ca != null) {
        // Preenche os campos com as informa칞칫es atualizadas do carro
        cxPlaca.setText(ca.getPlaca());
  
        cxCor.setText(ca.getCor());
        // ... preencher outros campos conforme necess치rio

        JOptionPane.showMessageDialog(
            null,
            "DADOS DO CARRO ALTERADOS",
            "ALTERA칂츾O DE CADASTRO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        limpar(); // Limpa os campos se o carro n칚o for encontrado
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "CARRO N츾O ENCONTRADO",
            JOptionPane.ERROR_MESSAGE
        );
    }
}

    public void delCarroPlaca() {
    Carro ca = new Carro();

    if (cxPlaca.getText().isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "Digite a placa para excluir o carro.",
            "Campo Vazio",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se o campo de placa estiver vazio
    }

    try {
        ca.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	ca =(Carro) ex.corPlaca(ca);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    // Comunica com GerCarro para excluir o carro pela placa
    ca = GerCarro.geraGerCarro().delCarroPlaca(ca);

    if (ca == null) {
        JOptionPane.showMessageDialog(
            null,
            "CARRO EXCLU칈DO",
            "EXCLUIR CARRO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "CARRO N츾O ENCONTRADO",
            JOptionPane.ERROR_MESSAGE
        );
    }
    limpar(); // Limpa os campos ap칩s a exclus칚o
}

   
   
