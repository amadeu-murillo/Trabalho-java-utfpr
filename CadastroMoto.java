    private boolean camposVazios() {
        return cxTarifa.getText().isEmpty()
                || cxDistancia.getText().isEmpty()
                || cxPlaca.getText().isEmpty()
                || cxCor.getText().isEmpty()
                || cxTipoCorrida.getText().isEmpty();
                
    }

    public void insMoto(){
        Moto mo = new Moto();
        
        if (camposVazios()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Preencha todos os campos para cadastrar o motorista.",
                    "Campos Vazios",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }
    try {
        mo.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	mo =(Moto) ex.corPlaca(mo);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }
        
        mo.setCor(cxCor.getText());
        
        mo.setTipoCorrida(cxTipoCorrida.getText());
    //-----------------------------------------------------------------
    boolean distanciaValida = false;
        while (!distanciaValida) {
            try {
                     mo.setDistancia(Double.parseDouble(cxDistancia.getText()));
                    distanciaValida = true; 
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(
                    null,
                    "A Dist칙ncia deve ser um numero.",
                    "Erro de Formato",
                    JOptionPane.ERROR_MESSAGE
                );



                cxDistancia.setText(JOptionPane.showInputDialog(null, "INFORME A DIST츽NCIA NOVAMENTE:"));
            }
        }
    //-----------------------------------------------------------------
       
    //--------------------------------------------------------------
    boolean tarifaValida = false;
        while (!tarifaValida) {
            try {
                    mo.setTarifa(Float.parseFloat(cxTarifa.getText()));
                    tarifaValida = true; // Define como verdadeiro se a convers칚o for bem-sucedida
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(
                    null,
                    "A tarifa deve ser um numero.",
                    "Erro de Formato",
                    JOptionPane.ERROR_MESSAGE
                );



                cxTarifa.setText(JOptionPane.showInputDialog(null, "INFORME A TARIFA NOVAMENTE:"));
            }
        }
    //--------------------------------------------------------------
        
        mo.setPassageiro(Integer.parseInt(cxQtdePassageiro.getText()));
        //arrumar
        mo = GerMoto.geraGerMoto().insMoto(mo);
        
        if(mo != null){
           JOptionPane.showMessageDialog(
                   null,
                   "MOTO CADASTRADA",
                   "Confirmar Cadastro",
                   JOptionPane.INFORMATION_MESSAGE
           
           );
        }
        else{
            JOptionPane.showMessageDialog(
                   null,
                   "ERRO AO CADASTRAR CARRO",
                   "ERRO",
                   JOptionPane.ERROR_MESSAGE
            );
        }
        limpar();
    }
