    public void consMoto() {
    Moto mo = new Moto();

    if (cxPlaca.getText().isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "Digite a placa para consultar o carro.",
            "Campo Vazio",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se o campo de placa estiver vazio
    }
    
        try {
        mo.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	mo =(Moto) ex.corPlaca(mo);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    mo= GerMoto.geraGerMoto().consMotoPlaca(mo); // Realiza a consulta do carro pela placa
    
    if (mo != null) {
        // Exibir informa칞칫es do carro encontrado nos campos correspondentes
        cxPlaca.setText(mo.getPlaca());
        cxCor.setText(mo.getCor());
        cxQtdePassageiro.setText(String.valueOf(mo.getQtdePassageiro()));
        cxTarifa.setText(String.valueOf(mo.getTarifa()));
        cxTipoCorrida.setText(mo.getCorrida());
        cxDistancia.setText(String.valueOf(mo.getDistancia()));

        // Exibir mensagem indicando que o carro foi encontrado
        JOptionPane.showMessageDialog(
            null,
            "CARRO ENCONTRADO",
            "CONSULTA DE MOTO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        // Limpar campos se o carro n칚o for encontrado e exibir mensagem de erro
        limpar();
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "MOTO N츾O CADASTRADA",
            JOptionPane.ERROR_MESSAGE
        );
    }
}

    public void altMoto() {
    Moto mo = new Moto();
    
    if (camposVazios()) {
        JOptionPane.showMessageDialog(
            null,
            "Preencha todos os campos para alterar os dados da moto.",
            "Campos Vazios",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se houver campos vazios
    }

         try {
        mo.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	mo =(Moto) ex.corPlaca(mo);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    mo = GerMoto.geraGerMoto().atualizaMoto(mo); // Atualiza o carro com base na placa fornecida
    
    if (mo != null) {
        // Preenche os campos com as informa칞칫es atualizadas do carro
        cxPlaca.setText(mo.getPlaca());
  
        cxCor.setText(mo.getCor());
        // ... preencher outros campos conforme necess치rio

        JOptionPane.showMessageDialog(
            null,
            "DADOS DA MOTO ALTERADOS",
            "ALTERA칂츾O DE CADASTRO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        limpar(); // Limpa os campos se o carro n칚o for encontrado
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "MOTO N츾O ENCONTRADA",
            JOptionPane.ERROR_MESSAGE
        );
    }
}

    public void delMotoPlaca() {
    Moto mo = new Moto();

    if (cxPlaca.getText().isEmpty()) {
        JOptionPane.showMessageDialog(
            null,
            "Digite a placa para excluir a moto.",
            "Campo Vazio",
            JOptionPane.WARNING_MESSAGE
        );
        return; // N칚o prosseguir se o campo de placa estiver vazio
    }

         try {
        mo.setPlaca(cxPlaca.getText());
    } catch (PlacaException ex) {
        ex.impErroPlaca();
	mo =(Moto) ex.corPlaca(mo);
        Logger.getLogger(CadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
    }

    
    mo = GerMoto.geraGerMoto().delMotoPlaca(mo);

    if (mo == null) {
        JOptionPane.showMessageDialog(
            null,
            "MOTO EXCLUIDA",
            "EXCLUIR MOTO",
            JOptionPane.INFORMATION_MESSAGE
        );
    } else {
        JOptionPane.showMessageDialog(
            null,
            "PLACA N츾O ENCONTRADA",
            "MOTO N츾O ENCONTRADA",
            JOptionPane.ERROR_MESSAGE
        );
    }
    limpar(); // Limpa os campos ap칩s a exclus칚o
}
